    @classmethod
    def get_role(cls, role_name: str) -> dict:
        """获取指定角色的配置"""
        role_name = role_name.upper()
        if hasattr(cls, role_name):
            return getattr(cls, role_name)
        else:
            return cls.BASE_ROLE

    @classmethod
    def create_custom_role(cls, name: str, system_prompt: str, temperature: float = 0.7, max_tokens: int = 4000) -> dict:
        """创建自定义角色"""
        return {
            "name": name,
            "temperature": temperature,
            "max_tokens": max_tokens,
            "system_prompt": system_prompt
        } 